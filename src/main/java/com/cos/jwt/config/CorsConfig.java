package com.cos.jwt.config;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.cors.CorsConfiguration;
import org.springframework.web.cors.UrlBasedCorsConfigurationSource;
import org.springframework.web.filter.CorsFilter;

@Configuration
public class CorsConfig {
	
	@Bean
	// 스프링 프레임워크가 들고있는 Cors 필터
	public CorsFilter corsFilter() {
		UrlBasedCorsConfigurationSource source = new UrlBasedCorsConfigurationSource();
		CorsConfiguration config = new CorsConfiguration();
		config.setAllowCredentials(true); //내서버가 응답을 할 때 json을 자바스크립트에서 처리할 수 있게 할지를 설정
		config.addAllowedOrigin("*");// 모든 ip 응답을 허용
		config.addAllowedHeader("*");// 모든 header 응답을 허용
		config.addAllowedMethod("*");// 모든 method(post, get , put ,patch) 응답을 허용
		source.registerCorsConfiguration("/api/**", config);
		return new CorsFilter(source);
	}
	
}
